fun main()
{
    var x=10
    var y=20
    //Arithmetic Operators
    println("${x+y} ,${x-y},${x/y},${x*y} ")
    //Assignment operators
    x+=5; y+=10;
    println("x+=5 is $x and y+=10 is $y")
    //Logical operators
    println("${x>y} ,${x<y} ,${x>=y} ,${x<=y}")
    //Relational Operator
    println("${x>0&&y<0}, ${x>0||y<0},${x!=0}")
}